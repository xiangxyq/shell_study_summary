#!/bin/bash

##1.4.1 1 read,能够从键盘读取字符串到一个变量，常用参数-p 提示内容 -t 是等待时间
echo "ex 1.4.1 #######"
echo "请输入你的名字："
read var
echo $var

read -p "请输入你的名字：" name
echo $name
#等待30s无输入跳过输入阶段，继续往下执行程序
read -p "请输入你的名字：" -t 30 name
echo $name
echo -e "-------------------------------------------------------------------------------------\n"
